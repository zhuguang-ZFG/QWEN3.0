#!/usr/bin/env python3
"""
GRBL Training Data Extractor
Reads source files from GRBL-related repos and generates Chinese explanations
using Claude API, saving training pairs to grbl_training_data.json.
"""

import os
import json
import time
import re
import urllib.request
from dotenv import load_dotenv
load_dotenv()

# ── Configuration ────────────────────────────────────────────────────────────

